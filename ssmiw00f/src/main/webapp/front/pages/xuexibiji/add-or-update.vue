												},
																																																																// 登陆用户信息
				user: {},
			}
		},
		components: {
			wPicker
		},
		computed: {
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
					},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
						// ss读取
																																	this.ruleForm.yonghuming = this.user.yonghuming
												this.ruleForm.xingming = this.user.xingming
															
																																																			
			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`xuexibiji`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
															if(o=='bijimingcheng'){
					this.ruleForm.bijimingcheng = obj[o];
					continue;
					}
																				if(o=='riqi'){
					this.ruleForm.riqi = obj[o];
					continue;
					}
																				if(o=='xuexijindu'){
					this.ruleForm.xuexijindu = obj[o];
					continue;
					}
																				if(o=='xuexixinde'){
					this.ruleForm.xuexixinde = obj[o];
					continue;
					}
																				if(o=='yonghuming'){
					this.ruleForm.yonghuming = obj[o];
					continue;
					}
																				if(o=='xingming'){
					this.ruleForm.xingming = obj[o];
					continue;
					}
																				if(o=='userid'){
					this.ruleForm.userid = obj[o];
					continue;
					}
														}
																																																															}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
																																																
			// 多级联动参数
																																																
															riqiChange(e) {
				this.ruleForm.riqi = e.target.value;
				this.$forceUpdate();
			},
																																				
																																													
																																													
																																																																		
			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
																																																																																																																																																																																																																																																																																								if((!this.ruleForm.bijimingcheng)){
					this.$utils.msg(`笔记名称不能为空`);
					return
				}
																																																																																																																																																																																																								if((!this.ruleForm.userid)){
					this.$utils.msg(`用户id不能为空`);
					return
				}
																																				if(this.ruleForm.id){
					await this.$api.update(`xuexibiji`, this.ruleForm);
				}else{
					await this.$api.add(`xuexibiji`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>

